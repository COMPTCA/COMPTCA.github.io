class _5_DoingArithmetic
{
	public static void main(String[] args)
	{
		int num = 100;
		int factor = 20;
		int sum = 0;

		sum = num + factor;
		System.out.println(sum);

		sum = num - factor;
		System.out.println(sum);

		sum = num * factor;
		System.out.println(sum);

		sum = num / factor;
		System.out.println(sum);
	}
}